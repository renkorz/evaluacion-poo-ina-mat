class Employee:
    def __init__(self, id_employee, name, position, salary, departament):
        self.id_employee = id_employee
        self.name = name
        self.position = position
        self.salary = salary
        self.departament = departament

def __str__(self):
    return (f"Empleado: {self.name}, Puesto: {self.position}, Salario: {self.salary}, Departamento: {self.departament}")